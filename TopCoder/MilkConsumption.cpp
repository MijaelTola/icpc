const long double eps = 1e-8;
class MilkConsumption {
public:
	vector <int> findMaxRegion(vector <int>, vector <int>);
};

vector <int> MilkConsumption::findMaxRegion(vector <int> population, vector <int> totalConsumption) {
    int n = population.size();
    long double mx = 0;
    for (int i = 0; i < n; ++i) 
        mx = max(mx, (long double)(1.0 * totalConsumption[i]) / population[i]);
    
    vector<int> ans;
    
    for (int i = 0; i < n; ++i) 
         if((long double)(1.0 * totalConsumption[i]) / population[i] == mx) ans.push_back(i);
    return ans;
}


