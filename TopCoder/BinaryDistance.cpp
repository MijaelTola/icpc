#include <bits/stdc++.h>


using namespace std;

class BinaryDistance {
public:
	int maxDist(int, int);
};

int BinaryDistance::maxDist(int N, int V) {
    
    if(N == 2) return 1;
    int d = 0;
    
    int v = V;
    set<int> s;
    while(v) {
        if(v != 1)s.insert(v);
        v >>= 1;
        d++;
    }
    long long l = V, r = N;
    
    while(l <= N) 
        l <<= 1;
    l >>= 1;
    
    bool flag = 1;
    int d2 = 0;
    while(r) {
        if(s.count(r)) flag = 0;
        r >>= 1;
        d2++;
    }
    long long dl = 1;
    while(dl < N) dl <<= 1;
    dl >>= 1;
    if(flag or dl < l) return (d - 1) + (d2 - 1);
    return (d - 1) + (d2 - 2);
}


