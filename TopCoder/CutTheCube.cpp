#include <bits/stdc++.h>

using namespace std;

class CutTheCube {
public:
	int findWinner(int, int, int);
};

int CutTheCube::findWinner(int L, int B, int H) {
    long long a = 1ll * L * B * H;
    if(a & 1) return 2;
    return 1;
}


double test0() {
	int p0 = 1;
	int p1 = 1;
	int p2 = 1;
	CutTheCube * obj = new CutTheCube();
	clock_t start = clock();
	int my_answer = obj->findWinner(p0, p1, p2);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p3 = 2;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p3 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p3 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
