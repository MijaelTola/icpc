#include <bits/stdc++.h>

using namespace std;

class NumberofFiboCalls {
public:
	vector <int> fiboCallsMade(int);
};

int a,b;
int dp[50];
long long f(int u) {
    if(u == 0) {
        a++;
        return 0;
    }
    if(u == 1) {
        b++;
        return 1;
    }
    
    return f(u - 1) + f(u - 2);
}
vector <int> NumberofFiboCalls::fiboCallsMade(int n) {
	a = 0, b = 0;
    f(n);
    return {a,b};
}


