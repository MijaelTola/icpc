#include <bits/stdc++.h>

using namespace std;

class EmoticonsDiv2 {
public:
	int printSmiles(int);
};

int dp[2010][2010];

int EmoticonsDiv2::printSmiles(int n) {
	for (int i = 0; i < 1010; ++i)
        for (int j = 0; j < 1010; ++j)
            dp[i][j] = 1e9;

    for (int i = 0; i < 2010; ++i)
        dp[n][i] = 0;

    for (int i = n; i >= 1; i--) {
        for (int j = 0; j <= n; ++j) {
            if(i + j <= n) dp[i][j] = min(dp[i][j], dp[i + j][j] + 1); //past
            if(i + i <= n) dp[i][j] = min(dp[i][j], dp[i + i][i] + 2); //copy and past
        }
    }
    
    return dp[1][0];
}


double test0() {
	int p0 = 2;
	EmoticonsDiv2 * obj = new EmoticonsDiv2();
	clock_t start = clock();
	int my_answer = obj->printSmiles(p0);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p1 = 2;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p1 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p1 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
