#include <bits/stdc++.h>

using namespace std;

class NicePartition {
public:
	int minCost(vector <int>);
};

int NicePartition::minCost(vector <int> H) {
	sort(H.begin(), H.end());
    int l = 0, r = H.size() - 1;
    int ans = 0;
    while(l < r) {
        ans += abs(H[l] - H[r]);
        l++, r--;
    }
    return ans;
}


double test0() {
	int t0[] = {0,2};
	vector <int> p0(t0, t0+sizeof(t0)/sizeof(int));
	NicePartition * obj = new NicePartition();
	clock_t start = clock();
	int my_answer = obj->minCost(p0);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p1 = 2;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p1 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p1 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
