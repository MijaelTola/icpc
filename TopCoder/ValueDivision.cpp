#include <bits/stdc++.h>

using namespace std;

class ValueDivision {
public:
	vector <int> getArray(vector <int>);
};


vector <int> ValueDivision::getArray(vector <int> A) {
	map<int, set<int> > mp;

    for (int i = 0; i < (int)A.size(); ++i) 
        mp[-A[i]].insert(i);

    for (auto &p: mp) {
        int val = -p.first;
        int cnt = 0;
        if(val == 1) break;
        if(p.second.size() == 1) continue;
        for (auto x: p.second) {
            mp[-(val - 1)].insert(x);
            cnt++;
            if(cnt == (int)p.second.size() - 1) break;
        }
        
        if(p.second.size() == 1) break;
        while(p.second.size() > 1) {
            p.second.erase(p.second.begin());
        }


    }
    
    vector<int> ans(A.size());

    for (auto p: mp) 
        for (auto x: p.second) 
            ans[x] = -p.first;

    return ans;
}


