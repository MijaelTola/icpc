#include <bits/stdc++.h>

using namespace std;

class MaximumBalances {
public:
	int solve(string);
};

int MaximumBalances::solve(string s) {
	
    int a = 0, b = 0;

    for (auto x: s) {
        if(x == '(') a++;
        else b++;
    }

    int total = min(a,b);
    return total * (total + 1) / 2;
}


double test0() {
	string p0 = "((((";
	MaximumBalances * obj = new MaximumBalances();
	clock_t start = clock();
	int my_answer = obj->solve(p0);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p1 = 0;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p1 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p1 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
