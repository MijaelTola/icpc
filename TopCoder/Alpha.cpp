class Alpha {
public:
	int maxPref(string);
};

int Alpha::maxPref(string s) {
    int c = 0;
	for (int i = 0; i < (int)s.size(); ++i) {
        if (s[i] == (char)(i + 'a')) c++;
        else break;
    }
    return c;
}


double test0() {
	string p0 = "abctyf";
	Alpha * obj = new Alpha();
	clock_t start = clock();
	int my_answer = obj->maxPref(p0);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p1 = 3;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p1 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p1 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
